'''
Operadores:
  
  (+) Suma
  (-) Resta
  (*) Multiplicación
  (/) División
  (%) Modulo o Residuo
  (//) Division con valor entero
  (**) Exponenciación
  
'''
suma = 10 + 3
print(suma)
resta = 10 - 3
print(resta)
multi = 10 * 3
print(multi)
division = 10 / 3
print(division)
residuo = 10 % 3
print(residuo)
entero_division = 10 // 3
print(entero_division)
exponenciacion = 10**3
print(exponenciacion)