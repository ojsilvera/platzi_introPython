print('este software verifica si el numero es par o impar')

# solicita el umero a evaluar y lo convierte en entero
num = int(input('digita numero: '))

# evaluo el modulo del numero ingresado'

# si es 0 el numero es par'

if num % 2 == 0:

  print(f'el numero {num} es par')

# si es != 0 el numero es impar

else:

  print(f'el numero {num} es impar')
